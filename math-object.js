// Math.random() ***very very important
// এই মেথড প্রতিবার কনসোল করলে একেকটা Random নাম্বার রিটার্ন করে। 0 থেকে 1 এর মধ্যে যেকোনো সংখ্যা সে রিটার্ন করে (যেমন, 0.2, 0.6, 0.9 ইত্যাদি)
{
  const a = Math.random(); // এটাকে প্রতিবার কনসোল করলে আলাদা আলাদা র‍্যান্ডম নাম্বার রিটার্ন করবে ০ হতে ১ এর মধ্যে (অর্থাৎ বেশিরভাগ সময়ই দশমিক সংখ্যা রিটার্ন করবে)
  console.log(a);
  console.log(Math.round(a)); //Math.random কে রাউন্ড ফিগার বানানোর নিয়ম এটা। a ভ্যারিয়েবলের মধ্যে Math.random কে রাখা হয়েছিল, এবার a কে Math.round এর ব্র্যাকেটে রাখলেই রাউন্ড ফিগার করে কখনো ০ আবার কখনও ১ রিটার্ন করবে কনসোলে।
}

{
  const s = Math.floor(Math.random()*10); //এখানে Math.random কে ১০ দিয়ে গুন করার ফলে এটা দশমিকের আগে এক ঘর বাড়বে (যেমন .2156489 এমন সংখ্যা থেকে 2.33658 এমন হয়ে যাবে। সেটাকে Math.floor করে রাউন্ড করা হয়েছে)
  console.log(s);
}

