// Math.max() method ***very important
{
  const a = Math.max(1,20,-105,-250,-63,36,58,42); // ব্রাকেটের মধ্যে যে আর্গুমেন্টগুলো দেওয়া আছে তাদের মধ্যে সবচেয়ে ম্যাক্সিমাম(হাইয়েস্ট) ভ্যালু বের করার জন্য Math.max() মেথড ব্যবহার হয়।
  console.log(a);
}



