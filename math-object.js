//*** বৃত্তের ক্ষেত্রফল বের করার নিয়ম ***/
//Area of Circle
// A = PI * r**2 //এটা হলো বৃত্তের ক্ষেত্রফল বের করার সুত্র, এখানে r**2 মানে r স্কয়ার
{
  const r = 3;
  const A = Number(Math.PI * r ** 2).toFixed(3);
  console.log(`This is the area of circle: ${A}`);
}

// বর্গমূল বা Square root বের করার নিয়ম
{
  const a = Math.sqrt(64); //Math.sqrt() এই মেথড দিয়ে বর্গমূল বের করা হয়, যে সংখ্যার বর্গমূল বের করতে হবে তাকে .sqrt এর ডানের ব্রাকেটে লিখতে হবে। ৬৪ এর বর্গ মূল ৮ (৮*৮=৬৪), তাই কনসোলে ৮ প্রিন্ট হবে
  console.log(a);
}

